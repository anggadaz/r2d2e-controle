    public void setDelete(boolean delete, FuncPertinence func) {
        if(!delete) {
            btAdd.setText("Adicionar");
        } else {
            btAdd.setText("Remover");
        }

        old = func;
        this.delete = delete;
    }

    public void clearFuncData() {
        tbParam.setText("");
        tbFunctionName.setText("");
        setDelete(false, null);
    }

