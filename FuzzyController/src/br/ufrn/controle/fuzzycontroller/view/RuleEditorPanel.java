    private void criarPanelInput(String nomeInput) {
        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel, BoxLayout.PAGE_AXIS));
        panel.setBackground(new java.awt.Color(255, 255, 255));

        JLabel label = new JLabel("<html>Se <font color=red>" + nomeInput + "</font> é:</html>");
        JList lista = new JList(new DefaultListModel());

        JScrollPane scroll = new JScrollPane();
        scroll.setViewportView(lista);

        JCheckBox chkBox = new JCheckBox("Não");
        chkBox.setBackground(new java.awt.Color(255, 255, 255));

        panel.setPreferredSize(new Dimension(15, 15));

        if (baseDados.getIn(nomeInput) != null) {
            for (FuncPertinence func : baseDados.getIn(nomeInput)) {
                ((DefaultListModel) lista.getModel()).addElement(func);
            }
        }

        ((DefaultListModel) lista.getModel()).addElement(NONE);

        panel.add(label);
        panel.add(scroll);
        panel.add(chkBox);

        listas.add(lista);
        chkNot.add(chkBox);

        panelIns.add(panel);
    }

    private void init() {
        panelIns.removeAll();
        listas = new ArrayList<JList>();
        ((DefaultListModel) lsitRules.getModel()).removeAllElements();
        lsitRules.clearSelection();

        ((DefaultListModel) listOut.getModel()).removeAllElements();
        listOut.clearSelection();

        if (baseDados.getOut(ConstantsFuzzy.VARIABLE_OUTPUT) != null) {
            for (FuncPertinence func : baseDados.getOut(ConstantsFuzzy.VARIABLE_OUTPUT)) {
                ((DefaultListModel) listOut.getModel()).addElement(func);
            }
        } else {
            for (Expression exp : baseDados.getExpressionOut(ConstantsFuzzy.VARIABLE_OUTPUT)) {
                ((DefaultListModel) listOut.getModel()).addElement(exp);
            }
        }

        for (String string : dataTypeIn) {
            criarPanelInput(string);
        }

        for (Rule regra : regras.getRules()) {
            ((DefaultListModel) lsitRules.getModel()).addElement(regra);
        }
    }

