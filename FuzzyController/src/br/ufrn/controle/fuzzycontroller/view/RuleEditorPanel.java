    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        Rule regra = new Rule();

        boolean inputValido = false;
        for (int i = 0; i < listas.size(); i++) {
            try {
                if (!listas.get(i).getSelectedValue().toString().equals(NONE)) {
                    regra.addPremise(dataTypeIn.get(i), chkNot.get(i).isSelected(), (FuncPertinence) listas.get(i).getSelectedValue());
                    inputValido = true;
                }
            } catch (NullPointerException ex) {
                continue;
            }
        }

        if (!inputValido) {
            return;
        }

        if (listOut.getSelectedValue() instanceof Expression) {
            regra.addFunctionOut(ConstantsFuzzy.VARIABLE_OUTPUT, jCheckBox3.isSelected(), (Expression) listOut.getSelectedValue());
        } else {
            regra.addFunctionOut(ConstantsFuzzy.VARIABLE_OUTPUT, jCheckBox3.isSelected(), (FuncPertinence) listOut.getSelectedValue());
        }

        regras.addRule(regra);

        ((DefaultListModel) lsitRules.getModel()).addElement(regra);
}//GEN-LAST:event_buttonAddActionPerformed

    private void buttonRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRefreshActionPerformed
        Rule regra = (Rule) lsitRules.getSelectedValue();

        boolean inputValido = false;
        for (int i = 0; i < listas.size(); i++) {
            if (!listas.get(i).getSelectedValue().toString().equals(NONE)) {
                regra.addPremise(dataTypeIn.get(i), chkNot.get(i).isSelected(), (FuncPertinence) listas.get(i).getSelectedValue());
                inputValido = true;
            }
        }

        if (!inputValido) {
            return;
        }

        if (listOut.getSelectedValue() instanceof Expression) {
            regra.addFunctionOut(ConstantsFuzzy.VARIABLE_OUTPUT, jCheckBox3.isSelected(), (Expression) listOut.getSelectedValue());
        } else {
            regra.addFunctionOut(ConstantsFuzzy.VARIABLE_OUTPUT, jCheckBox3.isSelected(), (FuncPertinence) listOut.getSelectedValue());
        }

        lsitRules.repaint();
    }//GEN-LAST:event_buttonRefreshActionPerformed

    private void lsitRulesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lsitRulesValueChanged
        Rule regra = (Rule) lsitRules.getSelectedValue();

        if (regra == null) {
            return;
        }

        for (int i = 0; i < listas.size(); i++) {
            FuncPertinence func = regra.getInputShape(dataTypeIn.get(i));
            listas.get(i).setSelectedValue(func, true);
        }

        FuncPertinence fp = regra.getOutPutShape(ConstantsFuzzy.VARIABLE_OUTPUT);
        if (fp == null) {
            listOut.setSelectedValue(regra.getOutPutExpression(ConstantsFuzzy.VARIABLE_OUTPUT), true);
        } else {
            listOut.setSelectedValue(fp, true);
        }

    }//GEN-LAST:event_lsitRulesValueChanged

    private void buttonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRemoveActionPerformed
        Rule regra = (Rule) lsitRules.getSelectedValue();
        if (regra != null) {
            regras.getRules().remove(regra);
            ((DefaultListModel) lsitRules.getModel()).removeElement(regra);
            lsitRules.repaint();
        }
    }//GEN-LAST:event_buttonRemoveActionPerformed

