    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        String real = txtReal.getText();
        String imaginario = txtImaginario.getText();

        Double numReal = Double.parseDouble(real);
        Double numImagi = Double.parseDouble(imaginario);

        polos.add(Complex.valueOf(SUtil.twoPlaces(numReal), SUtil.twoPlaces(numImagi)));

        listPolos.setListData(polos.toArray());
    }//GEN-LAST:event_buttonAddActionPerformed

    private void buttonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRemoveActionPerformed
        // TODO add your handling code here:
        int[] indices = listPolos.getSelectedIndices();
        for (int i : indices) {
            polos.remove(i);
        }
        listPolos.setListData(polos.toArray());
    }//GEN-LAST:event_buttonRemoveActionPerformed
