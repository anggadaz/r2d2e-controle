    public void WriteFile() {

        try {
            String pastaLog = "c:\\Logs-R2D2E";
            File diretorio = new File(pastaLog);
            diretorio.mkdir();

            String file = pastaLog + "\\log.txt";
            PrintStream ps = new PrintStream(file);
            System.setOut(ps);
            System.setErr(ps);

        } catch (Exception exception) {
            exception.printStackTrace();
        }
    }

